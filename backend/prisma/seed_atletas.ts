function gerarCPF(index: number): string {
  const num = String(index + 1).padStart(9, '0');
  return `${num.slice(0, 3)}.${num.slice(3, 6)}.${num.slice(6, 9)}-00`;
}

async function main() {
  console.log(`🔄 Cadastrando ${atletas.length} atletas...`);

  let criados = 0;
  let ignorados = 0;

  for (let i = 0; i < atletas.length; i++) {
    const nome = atletas[i];
    const cpf = gerarCPF(i);

    try {
      await prisma.atleta.create({
        data: {
          nome,
          cpf,
          dataNasc: '01/01/2000',
        },
      });
      criados++;
    } catch (e: any) {
      if (e.code === 'P2002') {
        console.warn(`⚠️  CPF duplicado para ${nome}, pulando...`);
        ignorados++;
      } else {
        throw e;
      }
    }
  }

  console.log(`✅ Concluído! ${criados} atletas cadastrados, ${ignorados} ignorados.`);
}

main()
  .catch((e) => {
    console.error('❌ Erro:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
